function legalTextToBlocks(source: string): LegalBlock[] {
  const blocks: LegalBlock[] = [];
  let pendingList:
    | { type: "orderedList"; items: string[] }
    | { type: "unorderedList"; items: LegalListItem[] }
    | null = null;

  const flushList = () => {
    if (!pendingList) return;
    if (pendingList.type === "orderedList") {
      blocks.push({ type: "orderedList", items: pendingList.items });
    } else {
      blocks.push({ type: "unorderedList", items: pendingList.items });
    }
    pendingList = null;
  };

  for (const rawLine of source.trim().split(/\r?\n/)) {
    const line = rawLine.trim();

    if (!line) {
      flushList();
      continue;
    }

    if (line.startsWith("## ")) {
      flushList();
      blocks.push({ type: "heading", text: line.replace(/^##\s+/, "") });
      continue;
    }

    const orderedMatch = line.match(/^\d+\.\s*(.+)$/);
    if (orderedMatch) {
      if (!pendingList || pendingList.type !== "orderedList") {
        flushList();
        pendingList = { type: "orderedList", items: [] };
      }
      pendingList.items.push(orderedMatch[1]);
      continue;
    }

    const nestedBulletMatch = rawLine.match(/^\s{2,}(?:・|- )\s*(.+)$/);
    if (nestedBulletMatch && pendingList?.type === "unorderedList" && pendingList.items.length > 0) {
      const lastItem = pendingList.items[pendingList.items.length - 1];
      if (typeof lastItem === "string") {
        pendingList.items[pendingList.items.length - 1] = { text: lastItem, children: [nestedBulletMatch[1]] };
      } else {
        lastItem.children.push(nestedBulletMatch[1]);
      }
      continue;
    }

    if (line.startsWith("・") || line.startsWith("- ")) {
      if (!pendingList || pendingList.type !== "unorderedList") {
        flushList();
        pendingList = { type: "unorderedList", items: [] };
      }
      pendingList.items.push(line.replace(/^(・|- )\s*/, ""));
      continue;
    }

    flushList();
    blocks.push({ type: "paragraph", text: line });
  }

  flushList();
  return blocks;
}
